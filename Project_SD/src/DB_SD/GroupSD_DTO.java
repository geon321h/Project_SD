package DB_SD;

public class GroupSD_DTO {

	private int group_no;
	private String group_name;
	private int group_manager_no;
	private String group_create_day;
	
	public int getGroup_no() {
		return group_no;
	}
	public void setGroup_no(int group_no) {
		this.group_no = group_no;
	}
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	public int getGroup_manager_no() {
		return group_manager_no;
	}
	public void setGroup_manager_no(int group_manager_no) {
		this.group_manager_no = group_manager_no;
	}
	public String getGroup_create_day() {
		return group_create_day;
	}
	public void setGroup_create_day(String group_create_day) {
		this.group_create_day = group_create_day;
	}
	
}
